local valor = 10
print("Eu tenho " .. valor .. " maças.")
valor += 5
print("Agora eu tenho " .. valor .. " maças.")
valor *= 2
print("Depois de uma promoção, eu tenho " .. valor .. " maças.")
valor -= 4
print("Depois de vender algumas, eu tenho " .. valor .. " maças.")
valor /= 2
print("Finalmente, eu tenho " .. valor .. " maças.")